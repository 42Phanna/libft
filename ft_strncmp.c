#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;
	int	tmp;

	i = 0;
	tmp = (int)n;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0' && tmp > i)
		++i;
	return (s1[i] - s2[i]);
}
