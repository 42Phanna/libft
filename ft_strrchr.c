char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		f;
	char	tmp;
	char	*tmp2;

	i = 0;
	f = 0;
	tmp = (char)c;
	tmp2 = (char *)s;
	while (tmp2[i] != '\0')
	{
		if (tmp2[i] == tmp)
			f = i;
		++i;
	}
	i = 0;
	return (tmp2 + f);
}
